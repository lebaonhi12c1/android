package com.example.lebaonhi_dh51904155.model;

public class classes {
    private int id;
    private String name;

    public classes() {
    }

    public classes(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
